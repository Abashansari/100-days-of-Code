public class Main {
	public static void main(String[] args) {
		boolean result1 = (5 > 2);
		boolean result2 = (10 == 15);
		boolean result3 = (true && false);
		boolean result4 = (true || false);

		System.out.println("Is 5 greater than 2? " + result1);
		System.out.println("Is 10 equal to 15? " + result2);
		System.out.println("true AND false: " + result3);
		System.out.println("true OR false: " + result4);
	}
}